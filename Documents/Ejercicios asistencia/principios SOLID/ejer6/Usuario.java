package ejer6;

public class Usuario {

    int id, identificacion, telefono;
    String nombre, direccion;

    public Usuario(int id, int identificacion, int telefono, String nombre, String direccion) {
        this.id = id;
        this.nombre = nombre;
        this.direccion = direccion;
        this.telefono = telefono;
        this.identificacion = identificacion;

    }

}
